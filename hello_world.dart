void main() {
    print('Hello, world!');
    String wizardName = 'Gandalf'; //text
    int magicLevel = 100;            //whole number 
    double spellPower = 75.5;         // decimal
    bool hasMagicStaff = true;        // true or false
    
    // use string interpolation to insert variables inside a string with the $ symbol
    print('Name: $wizardName');
    print('Magic Level: $magicLevel');
    print('Spell Power: $spellPower');
    print('Has Staff: $hasMagicStaff');
    
    // update a variable
    magicLevel = 110;
    print('New Magic Level: $magicLevel');
}


  

 